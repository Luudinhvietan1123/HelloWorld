package SelfLearn3_3_2020_StringAndArray;

public class OperationWithArrayAndString_3 {
    public static void main(String[] args){
        int[] arr = {1,2,3,4,5,6};
        for(int i = 0; i < 6; i++){
            System.out.println(arr[i]);
        }
        for(int j = 0;j < 6; j++){
            System.out.print(arr[j] + " ");
        }
    }
}
